package task4;

public class LengthTranslation {
    public static double convertingMetersToCentimeters(double value){
        return value*100.0;
    }
    public static double convertingMetersToMillimeters(double value){
        return value*1000.0;
    }
    public static double convertingMetersToKilometers(double value){
        return value/1000.0;
    }
    public static double convertingMetersToDecimeters(double value){
        return value*10.0;
    }
    public static double convertingCentimetersToMeters(double value){
        return value/100.0;
    }
    public static double convertingMillimetersToMeters(double value){
        return value/1000.0;
    }
    public static double convertingKilometersToMeters(double value){
        return value*1000.0;
    }
    public static double convertingDecimetersToMeters(double value){
        return value/10.0;
    }

}
